        <p>
          Au début il n'y avait rien.
        </p>
        <p>
          Ensuite il y a eu <i>PONG</i>.
        </p>
        <p>
          L'histoire d'une cause et d'un effet, d'une action et d'une réaction. Le pixel arrive vers la raquette du joueur, celui-ci la bouge pour répondre à cet acte. Presque par instinct. Souvent par réflexe. Toujours en réaction. Le pixel rebondit. Le pixel repart. La raquette en face réagit, et répond. Et juste comme ça, le jeu commence. Les jeux vidéo se mettent en marche.
        </p>
        <p>
          2009, plus de 35 ans après <i>PONG</i>, <i>BIT.TRIP BEAT</i> nous fait prendre conscience que rien n'a changé. C'est toujours l'histoire d'un effet, d'une cause, d'une action et d'une réaction. Les joueurs sont toujours devant les écrans, à attendre une cause, attendre un effet, pour ensuite faire une action, et attendre la réaction. Et ainsi de suite. Rien n'a changé. Les graphismes ont évolué, les histoires se sont complexifiées, les genres se sont diversifiés, mais tout cela n'est que du vent face à ce qui fait l'essence d'un jeu vidéo: le dialogue entre l'homme et la machine, l’interaction entre ces deux éléments reliés par des causes, des effets, des actions et des réactions de l'un vers l'autre, de l'autre vers l'un, pour ne faire qu'un finalement, dans un état second.
        </p>
        <p>
          <i>BIT.TRIP BEAT</i>, tout comme <i>PONG</i>, réduit cette situation à son essence-même et la met au cœur du jeu. Une raquette. Des pixels qui arrivent vers vous. Une réaction de votre part. Le jeu est lancé, il sera difficile de l'arrêter désormais. Presque naturellement, comme porté par l'ensemble, le joueur se retrouve embarqué dans un voyage cosmique qui parle de jeu vidéo, de musique, de Commander Video, de la vie, de vous enfin.
        </p>
        <p>
          Ouvrant le bal de la série de six jeux qui composent la saga du Commander Video, BEAT est une naissance.
        </p>
        <p>
          La naissance de ce petit personnage, créature noire qui vogue dans l'espace, qui va dès lors essayer de trouver sa place et évoluer dans cet univers étranger, tout en essayant de l'appréhender du mieux qu'il peut.
        </p>
        <p>
          C'est également la naissance du joueur dans le monde vidéo-ludique, balbutiant, agitant vaguement la raquette au début pour réagir aux quelques pixels qui arrivent, en ligne droite, sans peine, pour finalement grandir, à l'instar de Commander Video, et se transformer en une créature pensante. Une créature qui apprend, qui s'adapte, qui persévère, et devient finalement capable de soutenir les vagues les plus coriaces lancées à nous tout au fil du jeu par une mystérieuse force en action, de l'autre côté de l'écran.
        </p>
        <p>
          C'est la naissance du rythme enfin, le cœur du jeu, qui grandit et se complexifie jusqu'à devenir une musique assourdissante, composé de milliers de notes qui viennent mourir sur nous puis repartir de l'autre côté, créant à chaque fois un son et son écho, une action et une réaction.
        </p>
        <div class="img-inside">
          <img src="/img/img-inside/bit_trip_beat_in.jpg" alt="Capture d'écran du jeu"/>
          <div class="img-text">Aperçu du premier niveau</div>
        </div>
        <p>
          Pour décrire <i>BIT.TRIP BEAT</i>, on peut employer des mots, trouver des catégories: variation de <i>PONG</i>, jeu de rythme musical, jeu à pattern. Tout cela est vrai, et en même temps ce serait ne pas rendre justice à ce qu'est véritablement BEAT de se contenter de faire la somme de toutes ces définitions. BEAT est bien plus que cela, c'est une oeuvre qui ne peut pas convenablement s'expliquer. C'est un réflexe, c'est une action qui suit une autre action, c'est une succession de sentiments qui vont de l'émerveillement pur à l'euphorie d'un enfant qui découvre les joies de tenir seul sur un vélo, pour la première fois, en passant par toutes les étapes: frustration, désespoir, défaitisme, courage, ténacité, persévérance, satisfaction enfin.
        </p>
        <p>
          Composé de trois tableaux qu'il va falloir traverser en soufflant à peine au milieu, BEAT (et la série des BIT.TRIP dans son ensemble) a probablement un des univers les plus limpides et en même temps un des plus complexes affichés à l'écran. Il s'y passe beaucoup de choses, et peu de choses à la fois. Entre le score, les barres en haut et en bas qui sanctionnent votre progression (bonne ou mauvaise), votre raquette à gauche et les bits lancés sur vous à droite, sans compter l'arrière-plan, la multiplicité des sons qui se superposent en couches de rythme et de mélodies et les mouvements que vous devez faire pour que tout ça continue à marcher, il est vite facile de se laisser submerger et ne plus savoir où donner de la tête. Pour autant, il ne faut pas lâcher et persévérer. Les couleurs, les sons, cette sensation au bout des doigts, ce sourire débile de voir tout ces pixels rebondir contre notre raquette: tout cela est trop pour notre cerveau, trop beau pour nos yeux et nos oreilles, mais le voir s'en aller serait paradoxalement comme voir une sucette être confisquée par notre maman.
        </p>
        <p>
          Il ne faut pas, on ne veut pas.
        </p>
        <p>
          Bulle des sens, osmose parfaite entre l'action à l'écran et la réaction physique d'abord, via nos doigts, et auditive ensuite, via l'écho des pixels s'en allant pour toujours vers un ailleurs dont on ne saura jamais rien, <i>BIT.TRIP BEAT</i> délivre une synesthésie devenue rare dans le medium, une sensation d'assister à quelque chose de bien trop grand pour nous, mais qu'on arrive finalement à appréhender et maîtriser pour en saisir toute la signification et devenir à nouveau cet enfant sur son vélo, roulant à pleine vitesse pour la première fois.
        </p>
        <p>
          Alors oui, tout cela ne se fait pas en un jour. Il y aura des genoux en sang, et de nombreuses chutes. BEAT est un jeu extrêmement difficile rien que par son univers graphique à lui tout seul, mais c'est sans compter ses <i>patterns</i> et la dextérité requise pour venir à bout de toutes ces vagues projetées vers nous. Non pas que le jeu est vicieux; au contraire, <i>BEAT</i> est brillant dans son exposition de tous les types de bits qui occuperont l'écran, grâce à des codes couleurs et des sons qui nous expliqueront précisément l'effet de chaque élément. Il y aura les pixels orange qui rebondissent, les violets qui se déplacent en oscillant, les jaunes qui restent quelques secondes avant de repartir, les bleus qui marqueront un temps d'arrêt avant de reprendre leur course, et plein d'autres. Sur ce point, le jeu la joue franc-jeu avec nous. Non, la difficulté, tout simplement, c'est qu'il y a trop de choses à gérer en même temps, et que nous ne sommes finalement que des humains. "<i>I AM ONLY A MAN</i>", crie Commander Video, en écho avec notre sentiment face à l'écran du GAME OVER.
        </p>
        <div class="img-inside">
          <img src="/img/img-inside/bit_trip_beat_in_2.jpg" alt="Capture d'écran du jeu"/>
          <div class="img-text">Aperçu du mode NETHER</div>
        </div>
        <p>
          C'est alors que vient toute la puissance évocatrice de l'oeuvre, qui nous pousse à nous dépasser, à persévérer pour remonter sur ce beau vélo. Pas de raison que trois pixels et une raquette aient raison de moi. La gestion de la mort dans <i>BIT.TRIP BEAT</i> frôle le génie: chaque bit loupé émet un son désagréable qui vient perturber la belle mélodie et le beau rythme construit jusqu'à présent. Toujours plus de bits loupés, et le jeu se dégrade, littéralement: les visuels redeviennent très pixelisés, les sons retrouvent leur sonorité chiptune (pas que ce soit déplaisant, du reste), plus d'arrière-plan hallucinant. Et puis, si vraiment vous échouez, il y a le mode en noir et blanc. Là, dans une ultime référence à <i>PONG</i>, le jeu est morne. Sans musique, sans effet visuel.
        </p>
        <p>
          Nous sommes en 1972, et rien n'a changé.
        </p>
        <p>
          Il n'y a plus rien pour nous distraire.
        </p>
        <p>
          Mais,
        </p>
        <p>
          Il n'y a plus rien pour nous émerveiller.
        </p>
        <p>
          Pour peu que vous acceptiez de faire le voyage avec Commander Video, <i>BIT.TRIP BEAT</i> est une ode pour tout être humain, une ode qui résonne au fond du joueur car elle lui parle de choses qui comptent vraiment. Elle parle de la vie, de ses difficultés, de ses joies, de ses peines. Elle parle d'action et de réaction. Elle parle de nous, face à PONG, face à l'adversité. Elle parle de rythme, le rythme qui nous anime tous, qui fait bouger le monde, l'univers. La série des BIT.TRIP parle d'un voyage, celui que chacun fait, du début, jusqu'à la fin. <i>BIT.TRIP BEAT</i> en représente le début, et quel début.
        </p>
