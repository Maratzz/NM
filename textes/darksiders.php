        <p>
          Sortons dès le début le <i>caveat lector</i>: il ne sera question ni de <i>God of War</i> ni de <i>Zelda</i> dans ce texte. Cet appel aux gloires d’antan pour définir et consolider le titre de Vigil Games est tout juste une resucée du kit de presse fourni aux reviewers et autres journalistes vidéoludiques par le service des relations publiques de l’équipe de développement; il n’y a qu’à voir, ici ou ailleurs, combien ces deux jeux sont mentionnés l’un à côté de l’autre pour constater que la machine <i>marketing</i> a une nouvelle fois bien fait son travail pour conditionner et orienter l’angle d’approche sur l’oeuvre.
        </p>
        <p>
          Ne nous voilons pas la face, cependant: les ombres d’autres jeux planent bel et bien sur la carcasse de <i>Darksiders</i>, qu’on les mentionne ou non. Les inspirations — ou devrais-je plutôt dire les contrefaçons — sont présentes. Ici, des combats violents, sanguinolants. Là, des donjons axés sur un objet central. Des petits puzzles, une progression par paliers. On s’amuse, tel le Petit Poucet, à retrouver nos miettes de pain sur la carte du monde, jusqu’au dénouement, enfin.
        </p>
        <p>
          Mais de quel monde est-il question ?
        </p>
        <p>
          De quels objets parle-t-on ? Qui sont ces personnages si vides, si froids, si terriblement mal écrits, mal présentés ? L’univers de <i>Darksiders</i>, avant d’être une soupe de plusieurs genres et plagiats divers est avant tout un univers qui n’existe pas, un univers qui s’écroule dès l’introduction. A l’instar des cendres qui recouvrent cette New-York réinventée, dévastée par l’Apocalypse, les êtres qui peuplent ce monde s’envolent au moindre coup de vent. Ils ont l’épaisseur d’une feuille de papier, recouverts par quarante-cinq tonnes d’armures, d’épaulettes et autres épées démesurées. War est une tâche d’encre sur la feuille du scénariste, feuille qui se serait retrouvée emprisonnée dans une armoire à glace. Ça roule des épaules, ça gromelle, ça se veut indomptable, déchaîné, et pourtant quelle servitude dans ses actions, quelle propension à courber l’échine. Voilà un des quatre Cavaliers de l’Apocalypse réduit à faire du <i>search and fetch</i>, aux quatre coins de la carte, pour les besoins d’un scénario qui ne décolle pas, qui ne décollera jamais vraiment.
        </p>
        <p>
          On attend de <i>Darksiders</i> quelque chose, jusqu’à la fin du jeu. On attend que le monde devienne réel, que les personnages se mettent enfin à parler normalement, que l’histoire prenne de la hauteur,  que tout cela cesse d’être le rêve humide d’un jeune garçon bercé à la fantasy épique, aux épaulettes rutilantes, aux guerres incessantes entre anges et démons. On attend une vraie raison de prostituer comme on le fait le noble et autrefois inquiétant cavalier, pour l’envoyer récupérer des morceaux d’épées, des clés, des objets en tout genres. Tout ça pour quoi ? Chaque quête en amène une autre, puis une autre, puis une autre. C’est sans fin, mais ça n’a jamais eu de début pour commencer.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/darksiders_in.jpg" alt="Capture d'écran dans un donjon s'inspirant de l'architecture des cathédrales"/>
          <div class="img-text">Les environnements et l'architecture s'inspirent souvent d'un imaginaire eschatologique chrétien somme toute classique.</div>
        </div>
        <p>
          Propulsé, envoyé dans un monde de cendres 100 après l’Apocalypse, on constate que la seule préoccupation de ses habitants durant ce siècle aura été de bâtir des donjons étrangement adaptés à la morphologie de War. Les portes sont à son échelle, malgré ses épaules de boeuf; les coffres sont ici et là, prêts à être récoltés; et au milieu du donjon, un objet l’attend sagement, toujours à sa taille et utilisable immédiatement. On pourrait presque croire que l’univers n’existe que pour répondre aux besoins du cavalier.
        </p>
        <p>
          Que font les autres, à côté ? Vivent-ils ? Mangent-ils ? Ont-ils des préoccupations autre que la fin des temps ? Lorsque je me fatigue à récupérer un énième hommage à un autre jeu, vaquent-ils à leurs occupations ou m’attendent-ils là, benêts, attendant le script programmé pour faire avancer l’histoire ? Existent-ils par eux-mêmes, ou ne sont-ils finalement que des personnages de jeu vidéo&nbsp;?
        </p>
        <p>
          Ils ont tous tant de volumes, chacun plus gros que le précédent. C’est à qui sera le plus imposant, le plus méchant, le plus <i>badass</i>. Ennemis comme alliés. Quelle tristesse de constater que toute cette masse renferme un vide abysmal. On n’y croit pas une seule seconde, et en conséquence, on ne s’attache à personne. Aussitôt le dialogue obligatoire expédié, nous voici à nouveau sur la route. <i>Darksiders II</i> savait que ce manque de caractérisation ruinait le jeu et tentera par la suite d’inclure plus que deux lignes de discussions avec chaque protagoniste, pour essayer d’en apprendre plus et sympathiser avec tous ces êtres trop “stylés” pour être vrai. Le résultat ne sera pas plus concluant, mais on appréciera l’effort.
          War cherche à se venger d’une calomnie, nous dit-on.
        </p>
        <p>
          Il n’en sera rien. War, avant toute chose, détruit et massacre tout sur son passage. Dans un monde déjà ravagé, où les cendres des morts se confondent avec de la neige tellement il y en a, le cavalier se fraie un chemin avec son épée, utilisant encore et toujours les deux mêmes attaques. A la fin de l’envoi, il coupe. Avec ses deux tonnes d’armure, War est lent, poussif, mais les ennemis sont compréhensifs: ainsi, ils ne vous attaqueront jamais à plus d’une seule personne à la fois et attendront sagement leur tour en ligne, pour se faire éxécuter froidement. Durant un énième combat, alors qu’un démon était sur le point de m’asséner un coup, j’ouvre le coffre à côté de moi; il s’agit d’affirmer ses priorités, voyez-vous. L’ennemi, compréhensif, a immédiatement interrompu son animation pour me permettre de collecter le contenu du coffre. Déduisez-en ce que vous voudrez quant à la dynamique des combats.
        </p>
        <p>
          Ce n’est pas que massacrer des hordes d’êtres, si répugnants soient-ils, ne soient pas satisfaisant en soi, mais à vaincre sans panache, on triomphe sans gloire. Le panel de mouvements disponibles est en apparence riche mais illusoire. Deux attaques et un dash suffisent pour faire tout le jeu. Certains s’amuseront, s’entêteront à utiliser toutes les techniques disponibles: à quoi bon ? Ce ne sont que des variations d’un seul bouton. Un bouton pour les égorger tous, et dans la fatigue les amener. Il n’y a guère que les boss, malheureusement sans défis, pour nous sortir de la torpeur dans laquelle <i>Darksiders</i> nous plonge au fil de ses combats interchangeables, ses kilomètres de marche à pied et ses donjons aux milles et une portes. Et lorsque vient le cheval, l’aventure touche à sa fin déjà, sans avoir jamais vraiment démarré. En témoigne l’épilogue qui n’est qu’une manière d’annoncer le futur <i>Darksiders II</i>, sans finir quoi que ce soit de son côté. Mais, à nouveau, peut-on finir quelque chose qui n’a jamais commencé&nbsp;?
        </p>
        <p>
          <i>Darksiders</i> se laisse jouer, on ne lui enlèvera pas ça. Son interprétation de l’Apocalypse biblique, masculinisé comme jamais hélas, a au moins le mérite d’ouvrir la porte d’un imaginaire ravagé où l’enfer et le paradis se brisent l’un contre l’autre dans un maelström de violence. Voir toutes ces forces de la nature s’entrechoquer et soulever la terre comme jamais attirera les joueurs et joueuses bercés au biberon du too much américain, où on égorge d’abord et on pose les questions ensuite. <i>Darksiders</i> est un exutoire, un rassemblement de gueules cassées, une invitation au massacre. Le sang coule en abondance, l’esprit se fait rare, et l’âme est réduit à une monnaie d’échange; <i>Darksiders</i>, c’est l’histoire d’une copie de mondes vidéoludiques et fantasmés.
        </p>
