        <p>
          Une descente, une chute, une désacralisation du corps, de la vie précieuse. Susan Ashworth porte bien son nom et estime sa valeur à une poignée de cendres dispersées aussitôt au gré du vent. Personne ne se souviendra d’elle, de toute façon. Elle qui n’a pas d’amis, pas de liens ou de motivations, et qui préfère la compagnie de ses chats, elle préfère emprunter la petite porte des artistes, en silence et en toute discrétion. Une overdose de somnifères et déjà la réalité se trouble, laisse place à autre chose. Les murs disparaissent; le plancher de l’appartement tangue. Teacup l’observe, impassible, ses yeux en amande fixent le corps déjà froid de celle qui se faisait autrefois appeler <i>The Cat Lady</i>.
        </p>
        <p>
          Bientôt, Susan ne sera plus.
        </p>
        <p>
          Et lorsque sa vie semble soudain cesser, le jeu commence, orchestré par la Reine des Asticots, une figure aussi mystérieuse que dérangeante. Prisonnière d’un monde entre la vie et la mort, Susan n’a pas d’autre choix que celui d’accepter la proposition sournoise de la Reine, pour pouvoir enfin disparaître définitivement et fuir cette immortalité indésirée, indésirable, que son ennemie lui a conférée, cadeau empoisonné.
        </p>
        <p>
          Elle qui voulait mourir, la voilà condamnée à vivre. Pour son propre bien ?
        </p>
        <p>
          <i>The Cat Lady</i>, c’est l’histoire de Susan et de sa lente remontée des Enfers. C’est l’histoire d’une femme de 40 ans que la vie n’a pas épargnée et qui ne voit plus le monde qu’en déclinaisons de noir, de gris et de rouge. C’est l’histoire d’une reconstruction, au fil des rencontres et des dialogues, mais aussi le récit d’un combat contre soi-même, contre ces idées qui nous rongent de l’intérieur et ces gens qui nous dévorent de l’extérieur, parasites qu’il faut éliminer afin de retrouver le sourire.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/thecatlady_in.png" alt="Capture d'écran de Teacup, le chat de Susan dans le jeu"/>
          <div class="img-text">Teacup, le confident félin de Susan.</div>
        </div>
        <p>
          Jeu à la fois personnel et universel, <i>The Cat Lady</i> touche du doigt l’individu et les sentiments négatifs qui l’habitent et le hantent. Au cœur de l’oeuvre, on retrouve cette bile sans nom, ce dégoût qui survient parfois après une journée particulièrement désagréable; l’envie de tout casser autour de soi et de hurler au monde entier que rien ne va. Enfin, lorsque l’âme ne supporte plus de se regarder dans le miroir et plonge dans un océan de noirceur amère, spirale infinie d’idées sombres; lorsque l’on devient Susan Ashworth, le temps d’une journée, et qu’on embrasse sa personnalité, son humour pince-sans-rire et son ton désabusé, alors on comprend où veut en venir R. Michalski à travers cette histoire mélangeant romantisme morbide, visuels malsains, scènes du quotidien et effrayantes apparitions.
        </p>
        <p>
          Alternant entre une réalité troublante, fragile et son pendant fantasmagorique peuplé de créatures cauchemardesques, <i>The Cat Lady</i> fascine.
        </p>
        <p>
          L’image se déforme, se salit, se triture comme une véritable toile de fond soumise aux caprices de l’âme. Il y a un grain, une touche, une patte, c’est certain. Tour à tour collages, distorsions de motifs récurrents et fonds horrifiques, l’univers graphique du jeu nous plonge directement dans la réalité de Susan Ashworth, une réalité où le noir est une couleur dominante et où les corbeaux valsent à loisir au-dessus des dépouilles laissées dans le sillage.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/thecatlady_in_2.png" alt="Capture d'écran d'un dialogue entre Susan et la Queen of Maggots"/>
          <div class="img-text">L'arrière-plan fourmille d'images parfois subliminales.</div>
        </div>
        <p>
          C’est toujours avec une légère appréhension qu’on déambule dans les environnements du jeu, et on ne peut s’empêcher d’imaginer ce qui se cache derrière la porte ou au-delà de l’écran. Bien qu’étant très graphique par moments, l’oeuvre ne verse cependant jamais dans le voyeurisme et la facilité, soignant toutes les scènes, même les plus insoutenables. La frontière entre le morbide, le grotesque et l’étrange se trouve probablement quelque part dans <i>The Cat Lady</i>, qui délivre ici une univers oppressant, étouffant, à la limite du désagréable.
        </p>
        <p>
          Et c’est bien de ce sentiment dont il est question lorsque, en proie à des situations inhabituelles, nous cherchons les réponses. Il s’agit de retourner vers le familier, de fuir ce désagréable, cet <i>“uncanny”</i> qui habite tant l’aventure de Susan. Au fur et à mesure, celle qui se fait appeler la Dame aux Chats revient en terrain connu, retrouve sa vie et sa routine, pour mieux la sublimer et aller de l’avant, malgré les épreuves et les bâtons dans les roues.
        </p>
        <p>
          A travers une histoire qui mélange empathie, compréhension et rédemption, <i>The Cat Lady</i> aborde des thématiques profondes telles que la dépression, la solitude, le rapport aux autres et la violence psychologique, et les traite avec une finesse appropriée pour ces sujets-là.
        </p>
        <p>
          Tout au long du jeu, les mots volent et se heurtent aux chairs meurtries et lasses. Les dialogues, terrifiants de cohérence, sonnent beaucoup trop proches de la réalité et surprennent de par leur maturité. <i>The Cat Lady</i> est un jeu incroyablement humain, grâce à des personnages travaillés et une écriture au service d’un seul objectif: dépeindre la psyché et ses états d’âme, de la plus sombre à la plus joyeuse, en passant par toute la palette des émotions. Désespoir, mélancolie, humour, tendresse, colère, incompréhension. Tout y est.
        </p>
        <p>
          L’expérience, bien qu’étant centrée sur l’histoire et les situations, passe néanmoins par un <i>gameplay</i> classique de jeu d’aventure, croisé avec un <i>point-and-click</i>, ce qui n’handicape en rien le message transmis par l’oeuvre; au contraire, la simplicité des mécaniques de jeu permet de laisser l’univers graphique s’exprimer librement et permet au joueur d’être pleinement engagé dans l’histoire de Susan Ashworth.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/thecatlady_in_3.png" alt="Capture d'écran d'une transition dans le jeu avec une ampoule allumée et un papillon de nuit"/>
          <div class="img-text">La technique du collage est souvent employée pour ancrer l'univers dans un réalisme sombre.</div>
        </div>
        <p>
          Fragile mais combattante, dépressive mais lucide, sensible tout en étant impitoyable face aux parasites, Susan Ashworth est un être fascinant. Malmenée par la vie, par son mari, par les individus autour d’elle, elle trouve néanmoins la force de se relever à chaque fois et d’affronter ses peurs, ses angoisses et ses désillusions. La Dame aux Chats est belle, de cette beauté froide de ceux qui ont traversé la vie et ses aléas et en sont ressortis grandis. Comme les chats qu’elle affectionne particulièrement, Susan est noble, mystérieuse et hypnotisante.
        </p>
        <p>
          Son combat contre la dépression et ses démons intérieurs est une aventure dont on ressort légèrement différent. Loin des univers habituels vidéo-ludiques où la vie est souvent anecdotique, <i>The Cat Lady</i> ouvre la voie des jeux qui parlent à l’esprit et aux émotions profondes, celles que l’on cache et que l’on enfouit au risque de les laisser nous submerger et contrôler notre existence. L’oeuvre met des mots et des images sur des sujets qui nous touchent tous et dont on a peu l’habitude de parler dans ce médium. Plus qu’un jeu vidéo, <i>The Cat Lady</i> est une réflexion à ciel ouvert sur les aspects négatifs de la vie, et comment les surmonter, par quels moyens et avec quelle aide. Car il y aura toujours quelqu’un, quelque part, présent pour vous soutenir, même dans les moments les plus sombres.
        </p>
        <p>
          Après la pluie, le beau temps.
        </p>
