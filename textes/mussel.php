        <p>
          Tout ces petits détails, toute cette attention portée aux contrôles et à la manière dont le tir affecte nos mouvements, tous ces effets visuels et sonores, au creux de notre main, tout ça fait de <i>Mussel</i> un jeu hypnotique. C’est la manière dont les ennemis envahissent l’espace, dans un ballet chorégraphié, minuté au fil des blip blip chiptune de <a href="http://demoscenetimemachine.com/">David Whiting</a> (que vous pouvez écouter sur cette page); c’est la sensation de s’enfoncer toujours plus loin, toujours plus bas dans les abysses, à la découverte de créatures étranges, sans jamais savoir ce qui nous attend vraiment; et lorsque la confrontation finale pointe le bout de son nez, c’est avec une certaine appréhension qu’on positionne stratégiquement son sous-marin et qu’on canarde l’ultime créature, gigantesque, magnifique. Sa chute, synonyme de victoire, couronne l’expérience et nous permet d’assister à un bouquet final de feux d’artifice qui explosent devant nos yeux et nous laissent extatique, un sourire aux lèvres et les rétines brûlées par les explosions.
        </p>
        <p>
          A ce moment-là, tandis que l’écran vibre, tremble et que la créature pousse un dernier cri, à ce moment-là je me sens enfant à nouveau, devant ma vieille télévision cathodique, devant le boss de fin de <a href="https://nicolasmoisson.fr/textes/tailconcerto"><i>Tail Concerto</i></a>, ou celui d’<i><a href="https://nicolasmoisson.fr/textes/alundra">Alundra</a></i>. C’est une transmission d’énergie pure, de la machine à l’humain, du jeu au joueur. C’est l’essence même du jeu vidéo, une expérience enivrante. C’est une drogue douce, un bonbon pétillant. Encore trois minutes, juste une dernière fois.
        </p>
