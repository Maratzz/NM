        <p>
          De Droqen, ou Alex Martin dans la vraie vie véritable, vous ne connaissez sans doute pas grand-chose, et pour cause le bonhomme est relativement discret. Quelques petits jeux ici et là, <i>Probability 0</i>, ou zéro, et <i>Starseed Pilgrim</i>, qui reste à ce jour son plus grand “petit” succès. Le bougre ne fait pas de scandales, tient un petit blog, un twitter. Mais pas de violence, c’est les vacances. Droqen prend son temps, avance tranquillement, à l’ombre des pixels en fleur.
        </p>
        <p>
          Dans <i>Probability 0</i>, ou zéro, l’univers ne tient à pas grand chose, dans le fond comme dans la forme.
        </p>
        <p>
          Pour le fond, il y a quatre couleurs. Du noir, tout d’abord, beaucoup de noir, celui qui vous colle à la peau, celui des ennemis et des structures. Celui de l’écran de fin qui apparaît, inéluctable; du vert, ensuite, celui de ce puits infini et interminable qui ne cesse de descendre, vers où ? vers quoi ? nul ne le sait; du rouge, partout, celui du danger permanent qui rôde autour de vous, celui de vos yeux quand les probabilités de survie sont quasiment à 0, ou zéro; du blanc enfin, celui de l’espoir à travers le défilement hypnotisant des nombres en filigrane au centre de l’écran, celui des étoiles qui vous ouvrent des passages et vous procurent un court moment de répit. Celui de vos yeux, enfin, lueurs hagardes brillant dans l’obscurité, dans les profondeurs aléatoires.
        </p>
        <p>
          Pour la forme, il y a deux états: vivant ou mort.
        </p>
        <p>
          Vivant, les probabilités s’affolent, le compteur varie, fluctue au gré de vos déplacements, de vos chutes, de vos blessures. Mort, il ne vous reste plus que cette frustration, ce désespoir sourd au fond de vous, celui d’avoir presque réussi à battre le <i>highscore</i>, à aller plus loin, juste un tout petit peu plus loin. L’essence même de l’arcade et du jeu vidéo, en somme. Car si on devait introduire <i>Probability 0</i>, ou zéro, en un mot, ce serait tout d’abord <i>arcade</i>.
        </p>
