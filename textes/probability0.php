        <p>
          Comme je suis gentil, j’en rajoute trois autres: <i>arcade platformer rogue-like survival</i>. Comme des dés, mélangez-lez, secouez-les et lancez-les sur la table, et peu importe comment vous retournerez la chose, ce sera toujours le même résultat, un résultat fatidique: 0, zéro, une probabilité de zéro.
        </p>
        <p>
          D’abord les chances seront grandes, les possibilités infinies.
        </p>
        <p>
          Sauter, tomber, trouver le bon chemin, éliminer les ennemis, accumuler du pouvoir pour devenir plus fort au fil de la descente. Faire attention aux chutes de trop haut, surveiller les pièges, les ennemis explosifs. Toujours regarder ce compteur qui tourne en boucle, qui n’arrive pas à se fixer sur un nombre, sur quelque chose. Sa longueur rassure, mais sa présence irrite. Oh trois fois rien, juste une aiguille qui vous rappelle que la fin existe, tout simplement. Plus le temps passe, et plus le sentiment d’oppression augmente, tandis que les probabilités, elles, diminuent, inexorablement. Quelles probabilités au juste ? Tout défile trop vite pour vraiment saisir quoi que ce soit. Dans un moment d’inattention, on peut lire des messages cryptiques comme <i>“chance que vous revoyiez votre famille un jour”</i> ou bien <i>“possibilités d’atteindre quelque chose d’intéressant”</i>. Et ce nombre qui n’en finit pas de fluctuer, de se réduire en peau de chagrin. L’environnement est plus dense, les ennemis plus nombreux. La stratégie se corse, les erreurs s’accumulent, les milliers de chances deviennent des centaines, puis des dizaines. La musique, superbe, prenante, adaptée, s’intensifie, la fin est proche. Il faut lutter, cependant. Pour quoi faire, se demande-t-on dans un coin de la tête ? Déjà un boss se profile dans les profondeurs. Ainsi, ce sera par sa main que je mourrai cette fois-ci. A charge de revanche.
        </p>
        <p>
          <i>Probability 0</i> est un jeu déprimant et nihiliste.
        </p>
        <p>
          C’est peut-être le meilleur <i>survival</i>, et en même temps le pire, car vous aurez beau faire de votre mieux, vous échouerez. Toujours. C’est dans le titre. A partir de ce principe, il ne vous reste plus qu’à plonger, encore et encore, dans ce puits sans fond. Le fond, ce sera votre corps tombé sous les coups des probabilités.
        </p>
