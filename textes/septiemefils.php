        <h3>Publié le 23 avril 2015 - Cinéma</h3>
        <p>
          De la saga de l'<i>Epouvanteur</i>, que je n'ai, du reste, probablement jamais terminé à l'époque où j'étais au collège, je me souviens d'une atmosphère froide, glauque, un romantisme noir, un univers médiéval où les corps flottent au vent, pendus au bout des cordes, et où le héros passe toute une nuit dans une cave pour son premier test, seul, livré à sa claustrophobie et ses pensées. Je me souviens de l'Epouvanteur, un être étrange, bougon, empreint d'une aura inquiétante. Je me souviens d'Alice et sa relation avec le héros, une sorte de jeu du chat et de la souris qui n'en finit pas, une fille absolument bipolaire. Je me souviens de la mère de Tom, peut-être le personnage le plus ambigu du récit (d'après mes rares souvenirs), toujours à parler en prophéties et autres divinations.
        </p>
        <p>
          En souvenir de mes lectures adolescentes inachevées, j'ai regardé cette horreur cinématographique qu'est <i>Le Septième Fils</i>, qui a adopté un autre nom pour épargner la réputation de la saga d'origine et ne pas l'éclabousser avec son mauvais goût. C'est déjà ça. Sans même aborder la question de la fidélité aux livres et à l'univers original, le laideron qui nous est proposé ici ne parvient même pas <i>ex nihilo</i>, en tant que film de divertissement à nous proposer un résultat satisfaisant. Que reste-t-il des 95 millions de dollars de budget ? Une photographie hideuse où le flou lumineux est roi, des costumes du bal, du bal masqué ô laid ô laid, des effets spéciaux oscillant entre la cinématique vidéoludique et l'incrustation maladroite, et une bande d'acteurs qui doivent bien travailler pour manger, que voulez-vous, c'est la seule explication que j'ai trouvée pour justifier la présence de Jeff Bridges dans cette galère.
        </p>
        <p>
          Jeff Bridges à lui seul plonge le film dans une soupe à la grimace dont personne ne ressortira indemne; défiguré et avalant la moitié de ses syllabes, sa prestation de Bane sans le masque achèvera quiconque tentera bon gré mal gré d'accorder un peu d'attention aux dialogues insipides tout au long de ces 102 longues minutes de péripéties péripathétiques, cabrioles sans brio et autres scènes d'action traitées par-dessous la jambe. Faute d'avoir une histoire et des personnages intéressants, Le Septième Fils revient en terrain connu, terrain déjà foulé par Eragon, selon une théorie qui consiste à croire que montrer des dragons de synthèse à l'écran suffit pour cacher la misère en arrière-plan. Plus c'est gros mieux ça passe, dit-on, et nul doute que Sergei Bodrov a le yeux plus gros que le ventre sur ce coup-là.
        </p>
        <p>
          C'est tout de même triste de voir une nouvelle fois tout un univers tiré de la littérature pour adolescents sacrifié sur l'autel d'Hollywood et de l'argent-roi. Je ne suis même pas sûr que cette purge leur ait rapporté grand-chose, alors à quoi bon ? On me dit dans l'oreille que les bénéfices s'élèveraient autour de 14 millions, juste à peine pour commencer à chercher la prochaine victime à brûler sans procès. La prochaine fois, vous m'excuserez si je ne participe pas à l'exécution publique, ça commence à sentir le roussi, et ça me fait tousser.
        </p>
        <p>
          Mon seul amusement durant tout le film aura été de tergiverser sur l'identité de l'acteur qui joue le héros: c'est le prince Casse-Pieds de Narnia, oui ou non ?! Parfois, j'aime bien ne jamais reconnaître les acteurs, ça divertit pendant un mauvais film, c'est déjà ça.
        </p>
