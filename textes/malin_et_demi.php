      <article class="article" id="article">
        <h3>Publiée le 21 juillet 2018 - Écrite en mai 2016 - Fiction</h3>
        <h3>Cette histoire fut écrite dans le cadre d'un concours de nouvelle organisé par le CROUS en 2016. Il y avait deux contraintes&nbsp;: 4 pages maximum et un thème imposé, «&nbsp;sauvage&nbsp;».</h3>
        <hr />

        <p>« Il est huit heures.<br />
          — Il est l'heure alors.<br />
          — C'est l'heure.<br />
          — C'est une heure.<br />
          — Huit heures.&nbsp;»
        </p>
        <p>
          B. se lève lentement, péniblement. Sa peau le démange plus que d'habitude. Sur son bras gauche se dessinent de nouvelles contrées à mesure que l'épiderme brunit, ici et là. Les brûlures datent de cinq ans pourtant, mais elles ne cicatrisent jamais. On ne guérit pas de cela.
        </p>
        <p>« Aujourd'hui&nbsp;?<br />
          — « Lundi ».<br />
          — « Lundi ». « Début de la semaine ». « Travail ».<br />
          — « Travail ». Masque huit.»
        </p>
        <p>
          L'idée d'enfiler le masque le fait déjà frémir et une angoisse sourde grimpe le long de sa colonne vertébrale. Son dos d'accordéon se déplie, ses os craquent, et son bras s'étend, saisit le masque de saison accroché au mur, à côté des autres.
        </p>
        <p>
          Hiver, un lundi comme les autres, huit heures du matin.
        </p>
        <p>
          Dans la salle, ils lèvent tous les yeux vers lui, et le regardent enfiler le masque. Personne ne détourne le regard lorsque les crochets se plantent dans sa chair, autour du cou. Le métal qui tapisse l'intérieur de l'objet est glacial et l'effet est immédiat. La morsure du froid contre la peau autrefois brûlée le fait trembler, comme à chaque fois. Le masque est porté.
        </p>
        <p>« Le masque est porté, il faut le jouer.<br />
          — Il faut le jouer.<br />
          — Il faut le jouer. »
        </p>
        <p>
          B. hoche la tête, répète la formule. Un son rauque sort de sa gorge. Le sang qui perle et coule le long de sa nuque lui donne envie d'arracher le mécanisme, de décrocher les griffes et de hurler. Une furieuse envie de se gratter, de tout enlever. De quitter cette peau artificielle, de se frotter, comme un singe se frotterait pour enlever les puces sur son corps. Il tourne la tête et aperçoit C. Elle lève les mains vers ses joues et les fait remonter, pour mimer un sourire. Mimer seulement. Sa bouche ne s'étire pas, seule ses mains bougent et forment le croissant par-dessus les lèvres gercées. B. s'exécute, derrière les engrenages. Les cliquetis s'enclenchent. Les engrenages tournent. Sur la peau fraîche et dénuée d'imperfection de son masque, un sourire apparaît, révélant des dents blanches, propres, fausses.
        </p>
        <p>
          « Il est huit heures, la semaine reprend, voici l'humain qui s'en va au travail&nbsp;!&nbsp;»
        </p>
        <p>
          La voix vient de partout à la fois, elle fait trembler les murs et les tympans. Tout le monde s'agite dans la salle et se presse contre les murs, comme pour être hors de portée du son qui résonne et finit par mourir dans les coins, en silence. Une porte s'ouvre alors et la lumière derrière s'engouffre dans l'espace de la pièce, imprime un monolithe blanc qui s'étire sur le sol. B. vient se placer dessus, dans l'encadrement de la porte. Son ombre déforme aussitôt la projection au sol et peint en noir sur fond blanc les lignes de son corps osseux, fatigué, nu. Il attend désormais tout en se frottant le coude. Le désir de se gratter ne le quitte pas, jamais, ni lui, ni les autres. Dehors, c'est mal vu, alors il profite de pouvoir le faire ici une dernière fois. C'est un rituel.
        </p>
        <p>
          « Voici l'humain, tout sourire, qui dit au revoir à sa femme et à ses enfants et s'en va au travail&nbsp;!&nbsp;»
        </p>
        <p>
           B. se retourne et fait signe aux autres restés au fond de la pièce. Il mime un baiser et l'envoie à C., puis se remet en position, droit devant la porte.<br />
           Ses enfants, sa femme...ah&nbsp;!
        </p>
        <p>
          La première note de musique retentit.
        </p>
        <p>
          Autrefois il s'agissait de la chanson « Good Morning » tirée de la comédie musicale <i>Singin' In The Rain</i>. Maintenant, c'est le thème du lundi matin, huit heures, pour les humains qui partent au travail. B. lève le genou et s'engage sur l'allée pavée, dehors. Sa démarche est ample, exagérée, presque dansante. Il porte la main à son front et fait mine d'ôter un chapeau qui n'existe pas pour saluer un voisin. Celui-ci, simple statue en résine penchée par-dessus la fausse haie, ne lui répond pas. Rien n'oblige B. à faire ce geste, mais il le fait quand même à chaque fois qu'il sort. Une vieille habitude, ou peut-être une façon d'exprimer sa maigre liberté. Sur son masque, les yeux de bille roulent à droite et à gauche à mesure qu'il avance clopin-clopant. Le sourire reste figé, étiré, verrouillé, maintenu en dessous par des crochets qui blessent l'intérieur de ses joues et allongent ses lèvres gercées.
        </p>
        <p>
          Autrefois le sang dans sa bouche avait le goût du fer&nbsp;; maintenant il a le goût de lassitude.
        </p>
        <p>
          B. fait mine de chercher les clés de la voiture dans ses poches en mettant ses mains le long des cuisses et en les agitant. Au bout de quelques secondes, un bruit métallique se fait entendre. Il sonne aux oreilles de B. comme un trousseau de clés qui vient d'être trouvé. Il peut désormais passer à la prochaine imitation&nbsp;: le démarrage de la voiture. S'installant dans la machine qui fait office de véhicule, et qui n'est assurément pas une voiture, le voici qui pose les mains sur le volant et attend que l'engin démarre tout seul. C'est chose faite. À cheval sur un rail, la machine roule vers la gauche et avance lentement sur le circuit long d'une centaine de mètres. B. n'a besoin de rien faire. La radio sur le tableau de bord s'allume mais il n'écoute pas ce qu'elle émet. On y parle de météo, d'informations du jour et de côtes en bourse. Au-dessus de lui le ciel est peint en bleu. En son centre trône un projecteur gigantesque qui illumine la scène entière. Il n'émet pas de chaleur, juste une lumière de lundi matin.
        </p>
        <p>
          La voiture descend la rue et passe devant d'autres maisons. B. fait de temps en temps un signe de main vers d'autres comme lui qui montent dans leurs véhicules et s'apprêtent à partir au travail. Les sourires sur les masques sont tous resplendisssants. Les corps sont tous nus, décharnés, brûlées. C'est tout un quartier qui s'anime. Il finit toutefois par apercevoir la fin du décor et le tunnel derrière les derniers arbres, invisible pour les visiteurs derrière la vitrine. C'est là qu'il termine sa sortie du jour, après avoir simulé le départ au travail, huit heures. La voiture se gare dans le tunnel et la portière se lève. Le masque se décompresse et laisse échapper un peu de gaz. Les crochets se détendent. B. peut enfin cesser de sourire mais il ne relâche pas immédiatement ses lèvres, la transition trop brusque réveillerait les gerçures. À la place il se frotte les coudes. La démonstration est terminée, en tout cas pour lui. Caché par les arbres il peut observer les visiteur collés contre le verre, scrutant ici et là tous les détails de cette reproduction grandeur nature d'un quartier qui s'éveille en début de semaine.
        </p>
        <p>
          « Comme chaque lundi, la population humaine effectue ce rituel à l'échelle nationale&nbsp;!&nbsp;»
        </p>
        <p>
          Une voix commente les faits et gestes des autres personnes dans le décor. Certains ont pris la voiture, d'autres prennent leur temps, arrosent leur jardin, balaient devant la terrasse&nbsp;; tous suivent le scénario qui leur a été confié. Parfois le projecteur se concentre sur une zone en particulier et la voix en profite pour soulever un point à son propos.
        </p>
        <p>
          « Observez ici&nbsp;: cet homme n'a pas de voiture, il préfère aller au travail en bus&nbsp;!&nbsp;»
        </p>
        <p>
          Un bus aménagé pour laisser les visiteurs voir à l'intérieur surgit du sol et vient se loger sur le rail. À son bord, quelques individus, tous masqués, tous souriants, s'adonnent à la lecture de journeaux. L'homme qui attendait à l'arrêt monte dans le bus et fait mine de valider son ticket devant le conducteur. Ils se sourient mutuellement.
        </p>
        <p>
          « Dans ce véhiculer particulier, les humains s'entassent et font tous la même activité, mais chacun dans leur coin. Observez comment ils lisent tous le même journal mais ne communiquent pourtant jamais entre eux&nbsp;!&nbsp;»
        </p>
        <p>
          Un murmure d'admiration parcourt l'audience derrière la vitre. Certains se permettent de scanner le quartier avec leur troisième oeil, pour garder une image permenante du lundi, huit heures du matin. D'autres s'échangent des commentaires sur tel ou tel aspect particulièrement curieux du tableau qui s'anime devant eux. On pointe du doigt, on chuchote quelques plaisanteries sur l'accoutrement, les masques. La peau nue, brûlée, ne semble pas les troubler. Les plus jeunes des visiteurs appuient leur visage contre la vitre, béats d'admiration, et demandent à leurs parents si ce sont des vrais humains. Ceux-ci confirment, avec un air de satisfaction sur le visage. Ils ne regrettent pas d'être venus admirer cette exposition. Les rumeurs disaient donc vraies&nbsp;: ces sauvages vivaient bel et bien tout nus, exposés aux radiations. Ce n'était pas étonnant qu'ils aient si bien encaissé le bombardement planétaire. Si ils avaient su ça avant l'invasion, ils auraient peut-être utilisé autre chose. Une épidémie, ou une catastrophe naturelle.
        </p>
        <p>
          B. détourne le regard&nbsp;: une porte vient de s'ouvrir dans le tunnel. L'heure de retourner dans la salle d'attente, le cagibis, le vivarium. Il enlève le masque, lentement, péniblment. Sa peau ne le démange plus beaucoup à présent. Il marche dans le couloir qui conduit à la salle. Dedans, C. l'attend. Elle lui prend le masque des mains et va l'accrocher à côté des autres.
        </p>
        <p>
          « Comment étaient-ils&nbsp;?<br />
          — Nombreux.<br />
          — Curieux.<br />
          — Insouciants. »
        </p>
        <p>
          B. s'assoit auprès des autres et attrape son bol. La soupe est froide depuis le temps.
        </p>
        <p>
          « Demain&nbsp;?<br />
          — « Mardi ».<br />
          — « Mardi ». « Tondre le gazon ». »
        </p>
        <p>
          C. porte ses mains à ses sourcils et les incline. Elle fronce des mains.
        </p>
        <p>
          « Tondre le gazon.<br />
          — Masque trois. »
        </p>
