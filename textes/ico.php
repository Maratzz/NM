        <p>
          <i>ICO</i>, c’est tout d’abord une image qui évoque une apocalypse sereine, un monde déserté, une aire de jeu pour deux enfants se tenant la main envers et contre tout, tandis que leurs ombres projetées derrière eux s’étirent avec le soleil déclinant.
        </p>
        <p>
          La jaquette du jeu, à mi-chemin entre l’ocre de la terre et le bleu minéral du ciel, nous présente tout ce qu’il y a savoir sur l’œuvre. Quelques éléments d’architecture en premier et en arrière-plan: un moulin dont on ignore si il tourne ou pas dans cet univers apparemment immobile déserté par le vent lui-même; une échelle rouillée qui monte vers on ne sait où; des murs d’un blanc nacré réfléchissant la lumière du jour; une prépondérance des ombres qui s’étirent, forment des jours et des contre-jours, plongeant la scène dans une ambiance surréelle. Et au milieu de tout ça, ces deux êtres si petits, si fragiles, entourés par ces grands monolithes qui les observent en silence, dans leur quête d’un chemin, d’une sortie.
        </p>
        <p>
          Sur cette jaquette, on peut également voir la lutte acharnée de la lumière contre l’ombre. On peut apercevoir que les deux enfants marchent vers le soleil et contre l’obscurité froide de derrière les murs pour aller vers un ailleurs autrement plus lumineux, un ailleurs sans ombre pour les menacer. Ils vont vers la droite, ils se dirigent vers l’est, à la recherche de leurs origines, afin de savoir qui ils sont, et voir le lieu de naissance de ce soleil qui incarne l’espoir et la chaleur, loin de cette forteresse étouffante, oppressante, loin de ces cages et ces barreaux en fer, loin de cette prison ombragée dans laquelle ils étaient tout deux confinés.
        </p>
        <p>
          <i>ICO</i>, c’est l’histoire de leur libération et de leur fuite vers un monde meilleur, ensemble, tous les deux, main dans la main.
        </p>
        <p>
          <i>ICO</i>, c’est tout ça, et bien plus encore, et tout est visible directement sur la jaquette même du jeu. Il n’y a pas besoin d’expliquer ce qu’est ICO dans son essence: il suffit de se référer à l’image sur le devant de la boîte et on se retrouve immédiatement transporté dans l’univers de Fumito Ueda, un univers où la contemplation prend le pas sur l’action, où les questions ne trouvent pas de réponses ou si peu, et où le silence vaut plus que mille mots.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/ico_in.jpg" alt="Jaquette du jeu"/>
          <div class="img-text">La jaquette s'inspire du tableau "The Nostalgia of the Infinite" de Giorgio de Chirico.</div>
        </div>
        <p>
          Le silence dans <i>ICO</i> passe tout d’abord dans l’histoire presque muette. Un jeune garçon à cornes se retrouve injustement emprisonné dans une forteresse. Un des hommes qui l’accompagne vers sa cellule, un mystérieux sarcophage comme il en existe des dizaines dans la salle, murmure simplement que <i>« c’est pour le bien du village »</i>. Quel village ? Nous ne le saurons jamais.
        </p>
        <p>
          L’omerta est tenace.
        </p>
        <p>
          Nous ne saurons jamais rien des raisons qui ont poussé ces hommes à enfermer ce petit garçon ici. Le silence est d’or. Bâillonné dans le prologue, le joueur ne trouvera personne à qui parler durant tout le long du jeu, personne pour comprendre, personne à interroger. Personne, si ce n’est cette petite fille, là-haut, avec sa robe diaphane et son teint de perle, elle aussi enfermée dans une cage, pour une raison que nous ne saurons jamais.
        </p>
        <p>
          Oh, ils tentent bien de communiquer l’un avec l’autre, mais la langue n’est pas la même. La barrière des mots les empêche de savoir précisément ce qu’il en est véritablement. Et c’est le joueur qui se retrouve muet, incapable de formuler ces questions qui tournent dans sa tête: qui es-tu ? Que fais-tu ici ? Pourquoi es-tu enfermée ici ?
        </p>
        <p>
          Nous n’en saurons rien. Très vite, le silence revient entre eux et s’installe, sans jamais devenir étouffant, cependant. Ce n’est pas qu’ils n’ont rien à se dire, au contraire; mais la communication passera par des gestes, des appels, et ce contact permanent, cette main qu’il ne faudra pas lâcher.
        </p>
        <p>
          <i>“Yorda !”</i> semble crier Ico, tandis qu’il lui montre la sortie et l’observe grimper à l’échelle pour le rejoindre.
        </p>
        <p>
          <i>“Je ne peux pas !”</i> semble répondre Yorda, tandis qu’elle secoue la tête et observe son ami à cornes se démener pour trouver une solution.<br />
          Les cris et les appels se disputent aux sons des oiseaux et du vent pour briser ce silence mystique.
        </p>
        <p>
          C’est la forteresse toute entière dans laquelle le joueur va déambuler six heures durant qui semble être la gardienne même de ce silence presque religieux. Les murs, grands, majestueux, infranchissables, étouffent les bruits et retiennent les individus en leur sein. La pierre est partout, froide, dure, muette. Le lieu est désert, vide, abandonné. Quel fut le rôle de cette forteresse ? Pourquoi n’y a-t-il personne pour la garder ? Des questions, par milliers, mais pas de réponses. Juste un silence à peine interrompu par l’écho d’<i>ICO</i>.
        </p>
        <p>
          Quelques années avant <i>Shadow of the Colossus</i>, <i>ICO</i> pose les bases d’un univers où l’humanité semble avoir disparu et où seules l’architecture et les ruines semblent témoigner de l’existence d’une civilisation autrefois. A l’instar de Rapture dans <i>Bioshock</i>, c’est la forteresse toute entière dans laquelle déambulent Ico et Yorda qui est la pièce maîtresse de l’oeuvre. Au fil des heures, le joueur apprend à découvrir le lieu et ses mystères. Les murs, les ponts, les mécanismes, les poutres apparentes. Les échelles, les chaînes suspendues, les rebords glissants. De cette exploration ressort une certaine mélancolie face à ces moulins qui n’ont plus de meuniers, ou même ces prisons qui n’ont plus de geôliers. Où sont-ils tous passés ? L’univers est pour nous tout seul, semble dire le jeu, tout comme dans <i>Shadow of the Colossus</i> le monde est notre terrain de jeu et de chasse.
        </p>
        <p>
          Ici, pas question de chasser quoi que ce soit, si ce n’est la liberté.
        </p>
        <p>
          Au contraire, Ico et Yorda, et surtout cette dernière, se retrouvent dans le rôle des proies. Des ombres rôdent et cherchent activement à capturer la jeune fille pour la ramener vers sa mère, une mystérieuse Reine tout aussi impénétrable aux questions du joueur que le reste de l’œuvre. Qui est-elle ? Que veut-elle ? Difficile de répondre. Toujours est-il que ses sbires viendront régulièrement mettre des bâtons dans les roues des deux enfants. Le rythme du jeu se construit ainsi autour de phases d’exploration et de résolution des puzzles environnementaux pour progresser d’un côté et de phases d’affrontements contre les ombres de l’autre. Si les premières peuvent durer relativement longtemps et nous plongent dans une douce contemplation, les secondes sont rapides, imprévues, et de fait intenses, ce qui vient équilibrer les sensations, d’autant plus que les combats tournent moins autour de nous qu’autour de Yorda.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/ico_in_2.png" alt="Capture d'écran du jeu montrant Ico et Yorda en train de courir sur un pont"/>
          <div class="img-text">Par moments la caméra sublime la différence d'échelle entre les personnages et l'architecture de la forteresse.</div>
        </div>
        <p>
          La jeune fille, tout de blanc vêtue, et surtout la relation qu’elle entretient avec le joueur à travers Ico, et l’ensemble du jeu, tient ainsi de la symbiose et il n’y a rien de mieux que les phases de combat pour comprendre cette union spéciale qui relie les deux enfants. D’un côté, le garçon à cornes est à proprement parler invincible ; c’est tout juste si il va tomber lorsqu’une ombre le bousculera, avant de se relever plus ou moins rapidement. Ici, pas de barre de vie, pas de compétences. De l’autre côté, Yorda, si fragile, ne peut rien faire si ce n’est appeler à l’aide, avant d’être emportée par ces créatures de l’ombre qui veulent la ramener vers les ténèbres. Le jeu s’arrête immédiatement si par malheur ils parviennent à capturer celle dont il ne faut pas lâcher la main, ou si peu. Le jeu s’articule autour de Yorda, elle est la raison même de l’existence et du jeu, et de son contenu. Toutes les péripéties d’Ico ont pour unique but de permettre à la jeune fille de l’accompagner, de le suivre. Sinon, il est évident que l’agilité naturelle et les ressources inépuisables du jeune garçon lui permettraient de s’échapper assez facilement, mais parce que sa main est dans celle de son amie, de sa protégée, le voilà qui doit user d’inventivité et d’ingéniosité pour apprivoiser l’environnement et le modifier afin de le rendre accessible à Yorda.
        </p>
        <p>
          C’est l’histoire d’un garçon qui va changer le monde pour que la fille puisse le traverser et y vivre.
        </p>
        <p>
          Il en est de même pour les combats. C’est à peine si les créatures de l’ombre s’intéressent à nous tant elles sont obnubilées par cet être à la peau blanche comme le lait, et si jamais elles daignent nous prendre en compte, c’est uniquement parce que nous nous sommes sciemment interposés entre elles et leur cible. Mais en réalité, seule Yorda compte.
        </p>
        <p>
          C’est l’histoire d’une fille et d’une forteresse.
        </p>
        <p>
          C’est l’histoire d’un garçon et d’une forteresse.
        </p>
        <p>
          C’est l’histoire d’une fille et d’un garçon.
        </p>
        <p>
          C’est l’histoire d’une fille et de sa mère possessive.
        </p>
        <p>
          Tout va toujours par paire dans <i>ICO</i>, et cela va plus loin que le jeu puisqu’il est possible de trouver cette dualité, cette symétrie, avec <i>Shadow of the Colossus</i>, l’autre côté du miroir.
        </p>
        <p>
          <i>ICO</i> ne présente pour ainsi dire aucun affrontement majeur, si ce n’est un seul et unique au dénouement, et propose à la place une succession d’escarmouches avec des créatures faibles mais retorses. A l’inverse, <i>Shadow of the Colossus</i> ne sera qu’une suite d’affrontements gigantesques. A la forteresse étriquée qui forme l’unique environnement du premier jeu, le second propose des espaces ouverts s’’étendant à perte de vue, à travers montagnes, déserts et forêts. Yorda a les cheveux courts tandis que Mono les a longs. A l’immobilisme de cette dernière peut-on opposer la mobilité toute relative de la première, et ainsi de suite. Il ne s’agit pas ici de lister les éléments qui relient <i>ICO</i> à <i>Shadow of the Colossus</i>, tout juste quelques pistes pour aborder ces deux jeux tirés du même univers et apprécier les thèmes qu’ils dégagent, séparément ou ensemble.
        </p>
        <p>
          Car si les comparaisons profitent aux deux œuvres, force est de constater qu’<i>ICO</i> pris tout seul suffit déjà amplement pour satisfaire le joueur avide d’atmosphère et de poésie. A travers ce périple pour trouver une sortie, un lien particulier se forme avec Yorda et Ico, les deux enfants qui se tiennent la main. Voir le jeune garçon se pencher pour l’aider à monter un rebord de mur ou l’attraper après un saut au-dessus d’un fossé dangereux est touchant, et le travail effectué sur l’animation des deux personnages renforce les situations. On sent que le garçon à cornes se soucie réellement de son amie, et le contrôler pour le faire aller partout et soulever cubes et leviers respecte à la fois l’histoire interne du jeu et notre envie propre. Point de dissonance ludo-narrative dans ICO, jusqu’au HUD tout simplement inexistant, à part lorsqu’il faut sauvegarder.
        </p>
        <p>
          Si il fallait émettre quelques réserves purement techniques, on regrettera peut-être une maniabilité parfois hasardeuse qui pourra agacer par moment, mais cela est vite pardonné devant tout ce que nous offre le jeu.
        </p>
        <p>
          Car <i>ICO</i>, avant toute chose, c’est la promesse d’une aventure singulière mais à deux, une plongée dans un univers mélancolique fait de garçons à cornes et de jeunes filles éthérées luttant côte à côte, main dans la main, pour un meilleur avenir. Tour à tour touchant, sensible, délicat, stressant ou reposant, le voyage d’Ico et de Yorda se fait en silence et presque d’une traite, mais son souvenir résonne longtemps, en écho à celui de Wander et ses colosses, pour former finalement une expérience vidéoludique inoubliable.
        </p>
