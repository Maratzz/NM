        <p>
          Je n’ai à vrai dire jamais, ou très rarement, fréquenté de mon propre chef discothèques dansantes, concerts bruyants (pour ne pas dire braillards) et autres bars bondés sans qu’il n’y ait eu au préalable une incitation de la part de proches à les accompagner dans ce genre de lieux. L’expérience que j’y retire me rend invariablement et systématiquement mal à l’aise; l’impression de ne pas être dans mon élément ainsi qu’un désintérêt notoire pour les mélanges d’alcools improbables d’un côté — et l’effet recherché à travers eux — et la danse de l’autre font que je ne suis ni familier ni adepte de ces pratiques sociales. A cela se rajoute une autre raison, découverte en 2012 par le biais du fantastique jeu de Steven Lavelle: toutes les discothèques du monde paraissent fades et tristes en comparaison de celle de <i>Slave of God</i>.
        </p>
        <p>
          A travers ce dernier, les mots d’Oscar Wilde refont surface, 125 ans plus tard, notamment lorsqu’il soutient que <i>“la Vie imite l’Art beaucoup plus que l’Art n’imite la Vie”</i> dans son essai <i>The Decay of Lying</i> (<i>Le Déclin du Mensonge</i>, 1891):
        </p>
        <blockquote>
          A présent, les gens voient des brouillards, non parce qu’il y en a, mais parce que des poètes et des peintres leur ont enseigné la mystérieuse beauté de ces effets. Des brouillards ont pu exister pendant des siècles à Londres. J’ose même dire qu’il y en eut. Mais personne ne les a vus et, ainsi, nous ne savons rien d’eux. Ils n’existèrent qu’au jour où l’art les inventa.
        </blockquote>
        <p>
          C’est par ce même mécanisme que le monde entier se découvrit une passion soudaine pour les poisson-clowns à la sortie du film <i>Le Monde de Nemo</i> (Andrew Stanton, Lee Unkrich, 2003), ou bien que la publication des <i>Raisins de la Colère</i> (John Steinbeck, 1939) conduisit le Congrès à rectifier le droit du travail en pleine Grande Dépression. Non pas que les poissons rayés d’orange et de noir n’existaient pas avant 2003, ou bien que la situation de détresse économique ne frappait déjà pas les Etats-Unis au moment où Steinbeck utilise ce cadre pour son roman; mais, et c’est là toute la signification de la thèse de Wilde, <i>“le but raisonné de la vie est de trouver expression et […] l’Art lui offre certaines formes de beauté pour la réalisation de cette énergie”</i>.
        </p>
        <p>
          Explicitons ce point, avant de passer au suivant. Il n’y a aura jamais, même dans les rêves les plus fous, de discothèque plus hallucinée que celle de <i>Slave of God</i>, si bien que toute alternative “réelle” paraîtra édulcorée en comparaison. En quinze minutes, Stephen Lavelle saisit l’essence de ce genre de lieux et le retranscrit de telle sorte que même les personnes qui ne les fréquentent pas (comme moi) arriveront à ressentir l’atmosphère distillée ici. De même que les poisson-clowns achetés post-visionnage du film n’arriveront jamais à la nageoire de Nemo et de son père tant ils manqueront d’humour, de personnalité, de sensibilité — disons-le en un mot comme en cent, d’humanité, car c’est ça finalement dont il est question dans cette histoire aquatique, qui considère les animaux comme des miroirs tendus vers nous, des <i>vessels</i> dans le sens de vaisseau pour l’âme — les discothèques établies ici et là en ville ne pourront jamais proposer l’expérience de <i>Slave of God</i> tant elles manqueront de couleurs, de sons, de déséquilibre de l’oreille interne, de nausées euphoriques. Reprenant les mots de Wilde:
        </p>
        <blockquote>
          A lui [l’Art] les «formes plus réelles qu’un vivant», à lui les grands archétypes dont les choses existantes ne sont que d’imparfaites copies. […] Il peut dire à l’amandier de fleurir en hiver et faire tomber la neige sur le champ de blé mûr.
        </blockquote>
        <div class="img-inside">
          <img src="../img/img-inside/slaveofgod_in.png" alt="Capture d'écran sur la piste de danse dans le jeu"/>
        </div>
        <p>
          Cet univers fantasmé est trop plein pour être vrai, et c’est précisément ce qui le rend si satisfaisant. S’immerger dedans, le temps d’un quart d’heure, ressemble à une transgression de tout ce qu’on connaît, ce qui autrefois nous semblait être la limite du monde. Mais il y a derrière ce dernier un autre monde; derrière l’écran, un monde sans repère, sans images préconçues. Un monde dans lequel on se perd, faute d’indications — ou serait-ce par surplus d’indications, justement ? Slave of God assaille tous les sens, pénètre la chair jusqu’à produire des réactions physiques allant jusqu’au rejet et la nausée. Tel un Bowman sur-stimulé, absorbant toute la lumière de l’espace dans son voyage dans le monolithe, notre corps semble nous dire <i>“nous ne sommes pas équipés pour subir et assimiler un si grand influx d’informations à la fois”</i>.
        </p>
        <p>
          Je ne peux pas conclure ce billet sans évoquer le parallèle évident qui se dresse, suite à l’évocation du corps, avec le concept deleuzien de corps-sans-organes et une de ses propositions sous-jacentes, <i>“ce qui reste quand on a tout ôté”</i>¹. <i>Slave of God</i> est une exécution magistrale de cette idée, sur tous les plans. Si vous prenez une image mentale d’une soirée en boîte de nuit, et que vous enlevez tout sauf l’essence, le noyau ultime du concept, il vous reste la proposition d’Increpare, ni plus ni moins. Le sentiment d’être perdu. D’être seul. D’être aimé. D’être saoûl. D’être distrait par le présent, mais néanmoins inquiet pour l’avenir. Il y a tout ça dans <i>Slave of God</i>, et bien plus encore.
        </p>
        <p>
          Au milieu d’une production vidéoludique qui est restée essentiellement bloquée au figuratif et le mimétisme de systèmes établis dans le réel et assimilés par tous, l’oeuvre de Stephen Lavelle brille par son refus de verser dans l’automatisme des mécaniques, l’assurance du visuel et la clarté de la lecture symbolique. L’espace généré se joue des conventions, c’est par ailleurs une des motivations premières à l’intérieur du jeu; sortir de cet enfer est moins une condition pour “réussir” le jeu et gagner qu’un besoin crucial pour l’être humain aux commandes de retrouver ce qui fait sens. L’essentiel du propos se passe de mots, de dialogues ou d’indices. L’expérience se veut tactile, sensorielle, sensuelle même pourrait-on dire. La proximité des avatars est de rigueur: là, sur la piste de danse, il est nécessaire de maintenir un contact visuel avec l’être aimé pour espérer progresser, mais ce lien rend l’expérience plus insoutenable encore, ce qui se traduit par une perte des contrôles et une dégradation du champ de vision. On revient à Deleuze, une nouvelle fois, et sa déterritorialisation, pas si éloignée que ça du corps-sans-organes du reste. Il s’agit là de se détacher des idées communément acceptées, de perdre l’habitude, et d’encourager le bond vers l’inconnu pour défricher de nouveaux concepts pertinents pour nous modernes.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/slaveofgod_in_2.png" alt="Capture d'écran au moment où on est verrouillé avec notre partenaire sur la piste de danse"/>
        </div>
        <p>
          A ce titre, <i>Slave of God</i> est bien plus qu’une simulation de sentiments exacerbés. C’est une allégorie de notre ère de sur-information, de surabondance des sens. Son univers, censé nous faire lâcher prise et nous détendre, nous crispe en réaction et nous emprisonne en son sein. Il fait de nous un esclave, incapable de détourner le regard des couleurs extrêmes à l’écran, de ce liquide dans notre verre, ou de cette musique assourdissante. Esclave de Dieu, mais lequel ?
        </p>
        <p>
          ¹: <i>Mille Plateaux</i>, Gilles Deleuze et Félix Guattari, Editions de Minuit, 1980.
        </p>
