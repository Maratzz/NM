      <article class="article" id="article">
        <h3>Publié le 17 avril 2015 - Jeux Vidéo</h3>
        <p>
          Il y a des jeux qui sont définissables en une seule phrase, en tout et pour tout. On se croirait en pleine réunion, face au responsable; déjà son air sérieux et les deux regards qu’il a rapidement lancés vers sa montre nous indiquent qu’il n’a que quelques secondes à nous accorder, pas une de plus, et qu’il va falloir frapper fort, frapper bien. C’est l’idée du siècle ou rien.
        </p>
        <p>
          Alors on sort l’idée, en une phrase, en un mot, et tant pis pour la présentation dans les règles. Il faut <i>pitcher</i>, comme on dit désormais dans les milieux. Nous regardant droit dans les yeux, voici Tyler Glaiel qui se lance: <i>“Closure est jeu de puzzles où ce qui n’est pas éclairé n’existe pas”.</i>
        </p>
        <p>
          Une phrase, c’est tout ce qu’il faut pour saisir ce que <i>Closure</i> a à nous offrir. Alors ? Emballé ? Circonspect ? Franchement ennuyé ? L’avantage de l’idée-phrase réside dans sa brièveté, sa fraîcheur, sa politesse de ne pas prendre plus de place qu’il n’en faut pour la formuler, la manipuler et la saisir du bout des doigts. Son inconvénient, c’est son auto-mutilation: prisonnière du concept qui la définit, l’idée-phrase se transforme en jeu-phrase.
        </p>
        <p>
          <i>Closure</i> est un jeu-phrase.
        </p>
        <p>
          Il est court, il tient dans la main, et ne cherche jamais à aller plus loin que le point qui finit sa phrase. Il est question d’ombres et de lumières, d’absence et de présence, et il en sera question jusqu’au point final, jusqu’au dénouement. Autour de ce concept qui stipule que tout ce qui n’est pas éclairé n’existe physiquement pas, Tyler Glaiel tisse un jeu solide et efficace, rempli de surfaces à éclairer, de sol à illuminer, de clés à trouver, de portes à franchir.
        </p>
        <p>
          En l’espace de 24 niveaux par monde, trois au total et un dernier de 10 épreuves pour finir comme il se doit, <i>Closure</i> tourne autour de son idée, de son concept. Il est question d’obscurité; la majorité de l’écran sera perpétuellement plongé dans le noir, un noir peu engageant qui recule devant les faisceaux de lumière braqués ici et là. L’univers graphique du jeu se marie au thème et lui permet d’exprimer son potentiel jusqu’au bout. Il se dégage de cette absence de lumière quasi-permanente un sentiment d’oppression et d’emprisonnement. Puisque le sol n’est vraiment sol que lorsqu’il est suffisamment éclairé, il est d’autant plus facile de se sentir coincé sous la lampe, incapable d’aller ni à droite ni à gauche, sous peine de basculer dans le vide qui règne à l’écran. Le reset est par ailleurs facile tout au long des puzzles, que ce soit après une erreur de notre part ou une faiblesse au niveau du moteur physique qui fait parfois un peu des siennes et obligera le joueur à recommencer une séquence d’actions souvent longues, pour si peu.
        </p>
        <div class="img-inside">
          <img src="../img/img-inside/closure_in.jpg" alt="Capture d'écran dans le deuxième monde du jeu"/>
          <div class="img-text">Le jeu propose d'alterner entre trois personnages.</div>
        </div>
        <p>
          On sent que <i>Closure</i> insiste et veut approfondir son sujet, bien au-delà des visuels et des mécanismes. On devine derrière cette approche très saint-thomasienne en soi une réflexion plus intéressante sur la réalité, sa consistance et sa tangibilité. Lorsqu’un arbre tombe au milieu d’une forêt et que personne ne l’entend, peut-on dire que cela s’est vraiment passé ? Lorsque je fais face à la mer, qui me dit que le reste de la plage existe-t-il vraiment ? Et si le monde cessait d’être dès lors que je ne le regarde plus, et se matérialiserait à chaque fois devant mes yeux, mais uniquement devant ?
        </p>
        <p>
          Ce monde-là est le quotidien des personnages dans l’univers du jeu. Un univers sombre, littéralement, où rien n’est possible sans lumière. C’est, hélas, la limitation intrinsèque de l’oeuvre en tant que puzzle: puisqu’il est absolument vital d’éclairer les éléments pour les manipuler et les amener aux bons endroits, les possibilités au sein même des niveaux ne sont pas infinies. Le jeu-phrase est prisonnier de ses mots et des virgules qui le ponctuent. Il y a bien un moment de flottement, au tout début d’un niveau, lorsqu’il s’agit d’en faire le tour pour se faire une idée mentale de la pièce, des éléments qui sont dedans, mais l’hésitation vacille comme une flamme de bougie dès lors qu’il s’agit de mettre en pratique le concept, ce concept-maître, cette unique vérité: tout ce qui n’est pas éclairé n’existe pas. A partir de là, ce n’est pas bien difficile de comprendre où le développeur veut en venir. Orienter la lampe ici, prendre la clé, incliner cette autre lampe là, sauter de surface en surface: <i>Closure</i> a par moments le goût mécanique d’un casse-tête en bois, froid, sombre. Les pièces s’emboîtent, les portes s’ouvrent, et l’expérience de jeu se réitère dans le niveau suivant.
        </p>
        <p>
          Ce n’est pas la faute du jeu, encore une fois. C’est tout juste sa malédiction inhérente d’être un jeu-phrase, une idée-concept, un <i>one-trick pony</i>. Ça, couplé à sa volonté d’être cryptique, pour coller à l’univers monochromatique, fait de <i>Closure</i> un jeu typiquement artistique, artistiquement typique, un jeu à prendre avec des pincettes; un jeu où la motivation première pour le finir oscille entre la fierté de faire le travail jusqu’au bout et la curiosité d’en voir un tout petit plus. L’ennui n’est pas loin cependant, et nul doute que quelques-uns au fond de la classe piqueront du nez devant l’accumulation de salles faussement similaires, ou si peu, la musique de fond un tantinet répétitive, et l’histoire éparpillée ici et là, flottant autour des sources de   lumières telle des papillons de nuit.
        </p>
        <p>
          Pour les autres, la fraîcheur du concept séduira, même si les habitués des puzzles resteront sur leur faim après avoir terminé l’aventure. Ce n’est pas que la phrase ne fut pas plaisante à lire, au contraire; mais ça ne sera, définitivement et à jamais, qu’une seule et unique phrase, au milieu d’un monde de textes et de romans vidéo-ludiques.
        </p>
